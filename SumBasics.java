import java.util.*;
public class SumBasics {

    public static void main(String args[]) {

        Scanner scan = new Scanner(System.in);
        System.out.print("Enter number A : ");
        int A = scan.nextInt();
        System.out.print("Enter number B : ");
        int B = scan.nextInt();
        int Sum = A+B;
        System.out.println("Sum of A and B is : " + Sum);

    }
}
